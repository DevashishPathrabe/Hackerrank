def quickSort(arr):
    # Write your code here
    pivot = arr[0];
    for i in range(1, len(arr)):
        if pivot > arr[i]:
            for j in range(i, 0, -1):
                temp = arr[j]
                arr[j] = arr[j-1]
                arr[j-1] = temp
    return arr

